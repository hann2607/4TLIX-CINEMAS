    private void RunSlide() {
        JLabel ListLabelSlide[] = {lbl_Slide1, lbl_Slide2, lbl_Slide3, lbl_Slide4};
        Thread slideThread = new Thread() {
            @Override
            public void run() {
                try {
                    while (true) {
                        if (indexSlide < ListLabelSlide.length - 1) {
                            indexSlide++;
                        } else {
                            indexSlide = 0;
                        }
                        sleep(3000);
                        pnl_SlideMain.setPanelSlider(20, 5, ListLabelSlide[indexSlide], RSPanelsSlider.DIRECT.LEFT);
                    }
                } catch (Exception e) {
                }
            }
        };
        slideThread.start();
    }

    boolean isNextSlide = true;
    int indexSlide = 0;

    private void nextAndPrevSlide() {
        JLabel ListLabelSlide[] = {lbl_Slide1, lbl_Slide2, lbl_Slide3, lbl_Slide4};

        //Check người dùng nhấn next hay prev slide
        if (isNextSlide) {
            if (indexSlide < ListLabelSlide.length - 1) {
                indexSlide++;
            } else {
                indexSlide = 0;
            }
            pnl_SlideMain.setPanelSlider(20, 5, ListLabelSlide[indexSlide], RSPanelsSlider.DIRECT.LEFT);
        } else {
            if (indexSlide == 0) {
                indexSlide = ListLabelSlide.length - 1;
            } else {
                indexSlide--;
            }
            pnl_SlideMain.setPanelSlider(20, 5, ListLabelSlide[indexSlide], RSPanelsSlider.DIRECT.RIGHT);
        }
    }

    public void addToPanelAccMana(List<NHANVIEN> listNV) {
        Font font = new Font("SansSerif", Font.BOLD, 12);
        pnl_AccountMana_ShowListAcc.removeAll();
        for (int i = 0; i < listNV.size(); i++) {
            if (listNV.size() < 4) {
                pnl_AccountMana_ShowListAcc.setLayout(new GridLayout(4, 2));
            } else {
                pnl_AccountMana_ShowListAcc.setLayout(new GridLayout(listNV.size() / 2 + 1, 2));
            }
            try {
                // panel
                JPanel pnl = new JPanel();
                pnl.setLayout(new GridLayout(1, 2));

                // Add Image 
                JPanel pnlImage = new JPanel();
                pnlImage.setBackground(new Color(255, 255, 255));
                JLabel lbl = new JLabel();
                lbl.setSize(100, 100);
                File f = new File("");
                String duongdanBanner = "\\src\\main\\resources\\com\\qlrp\\icon\\Employee\\ImageEmployee\\";
                lbl.setIcon(XImage.ResizeImage(lbl.getWidth(), lbl.getHeight(), f.getAbsolutePath() + duongdanBanner + listNV.get(i).getHINH()));
                pnlImage.add(lbl);

                // Add Id, name, Role
                JPanel pnlInfo = new JPanel();
                pnlInfo.setBackground(new Color(255, 255, 255));
                JLabel ID = new JLabel("MÃ: " + listNV.get(i).getMA_NHAN_VIEN());
                JLabel fullName = new JLabel("HỌ VÀ TÊN: " + listNV.get(i).getHO_TEN());
                JLabel Role = new JLabel("QUYỀN: " + listNV.get(i).getTEN_VAI_TRO());
                JLabel Gmail = new JLabel("GMAIL: " + listNV.get(i).getEMAIL());

                // Set font
                ID.setFont(font);
                fullName.setFont(font);
                Role.setFont(font);
                Gmail.setFont(font);

                // Add Id, name, role to panel
                pnlInfo.setLayout(new GridLayout(4, 1));
                pnlInfo.add(ID);
                pnlInfo.add(fullName);
                pnlInfo.add(Gmail);
                pnlInfo.add(Role);

                // Add property for panel
                pnl.add(pnlImage);
                pnl.add(pnlInfo);

                // Set color panel
                pnl.setBackground(new Color(255, 255, 255));
                pnl.setBorder(BorderFactory.createLineBorder(new Color(255, 255, 255), 10));

                // Add event show form edit when click on panel
                NHANVIEN epl = listNV.get(i);
                pnl.addMouseListener(new MouseAdapter() {
                    @Override
                    public void mousePressed(MouseEvent e) {
                        if (e.getClickCount() == 1 && !e.isConsumed()) {
                            NewEmployee npl = new NewEmployee();
                            npl.setVisible(true);
                            npl.setEnableButton("edit");
                            npl.setForm(epl);
                            getInfo.nv = epl;
                        }
                    }
                });

                // Add panel to panel Parent
                pnl_AccountMana_ShowListAcc.add(pnl);
            } catch (Exception e) {
            }
        }
        pnl_AccountMana_ShowListAcc.revalidate();
        pnl_AccountMana_ShowListAcc.repaint();
    }

    public void fillToTable_DA(List<DOAN> listDA) {
//        clear table va do du lieu len table
        tbl_QLDA.removeAll();
        model = (DefaultTableModel) tbl_QLDA.getModel();
        model.setRowCount(0);
        listDA = new QLDADAO().selectAll();
        for (DOAN da : listDA) {
            model.addRow(new Object[]{da.getTENDOAN(), da.getHINH(), da.getKICHCO(), da.getSOLUONG(),
                da.getDONGIA(), da.getMAKHUYENMAI()});
        }
        // show IMG product

        tbl_QLDA.getColumnModel().getColumn(1).setCellRenderer(new ImageRenderer());
    }

    private class ImageRenderer extends DefaultTableCellRenderer {

        //add img into table
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            String photoname = String.valueOf(value);
            ImageIcon ImageIcon = new ImageIcon(
                    new ImageIcon("src/main/resources/com/qlrp/image/doan/" + photoname).
                            getImage().getScaledInstance(60, 50, Image.SCALE_SMOOTH));
            //add border for img
            JLabel lb = new JLabel(ImageIcon);
            lb.setBorder(BorderFactory.createLineBorder(Color.BLACK, 1));
            return lb;
        }
    }

